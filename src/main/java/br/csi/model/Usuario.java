package br.csi.model;

public class Usuario {
	
	private int id;
	private String nome;
	private String login;
	private String senha;
	private String tipo;
	private String cidade;
	private String trabant;
	private String trabatual;
	private String  crm;
	private String cpf;
	private String email;
	private String sobrenome;

	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSobrenome() {
		return sobrenome;
	}
	public void setSobrenome(String sobrenome) {
		this.sobrenome = sobrenome;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getTrabant() {
		return trabant;
	}
	public void setTrabant(String trabant) {
		this.trabant = trabant;
	}
	public String getTrabatual() {
		return trabatual;
	}
	public void setTrabatual(String trabatual) {
		this.trabatual = trabatual;
	}
	public String getCrm() {
		return crm;
	}
	public void setCrm(String crm) {
		this.crm = crm;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}



}
